def load_part2_tsv() -> list[tuple[str, str, str, str]]:
    """Additional rows from scripts/receiving_criteria_part2.tsv (UTF-8, tab-separated)."""
    path = Path(__file__).resolve().parent / "receiving_criteria_part2.tsv"
    if not path.is_file():
        return []
    out: list[tuple[str, str, str, str]] = []
    with path.open(encoding="utf-8", newline="") as f:
        reader = csv.reader(f, delimiter="\t")
        for row in reader:
            if not row or not any(cell.strip() for cell in row):
                continue
            if row[0].strip().startswith("#"):
                continue
            while len(row) < 4:
                row.append("")
            out.append((row[0].strip(), row[1].strip(), row[2].strip(), row[3].strip()))
    return out


